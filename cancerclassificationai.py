import pandas as pd
dataset = pd.read_csv('cancer.csv')

x = dataset.drop(columns=["diagnosis(1=m, 0=b)"])

y = dataset["diagnosis(1=m, 0=b)"]

# split our data into a training set and a testing set

from sklearn.model_selection import train_test_split

x_train,x_test,y_train,y_test = train_test_split(x,y, test_size=0.2)

import tensorflow as tf

model = tf.keras.models.Sequential()

#Adding Layers to our model

model.add(tf.keras.layers.Dense(256, input_shape = x_train.shape, activation = 'sigmoid'))
model.add(tf.keras.layers.Dense(256, activation = 'sigmoid'))
model.add(tf.keras.layers.Dense(1, activation = 'sigmoid'))

#Compiling My model using an optimizer

model.compile(optimizer='adam', loss='binary_crossentropy',metrics=['accuracy'])

#Fitting Our Data

model.fit(x_train,y_train, epochs=1000) #epochs: no of times the algo is iterating over the same data. This is neccessary for learning

# Evaluate our Algo

model.evaluate(x_test,y_test)

